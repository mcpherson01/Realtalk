    /**
     * get all stopwords from db
     * @global wpdb $wpdb
     * @return array
     */
    public function getAll()
	{
		global $wpdb;
		return $wpdb->get_col("
			SELECT keyword
			FROM `".$this->getTableName()."` as t");
	}
    
    /**
     * get stopword by id
     * @global wpdb $wpdb
     * @param int $id
     * @return stdClass
     */
	public function getById($id)
	{
		global $wpdb;
		return $wpdb->get_row("
			SELECT *
			FROM `".$this->getTableName()."` as t 
            WHERE id = " . $id);
	}
 
    /**
     * update stopword, save result to DB
     * @global wpdb $wpdb
     * @param int $id
     * @param array $info
     * @return bool
     */
    public function updateStopKeyword($id, $info)
	{
		global $wpdb;
		return $wpdb->update( $this->getTableName(), 
                array( 'updated' => current_time('mysql')) + $info,
                array('id' => $id));
	}
    
    /**
     * save stopword to DB
     * @global wpdb $wpdb
     * @param array $info
     * @return bool|int
     */
	public function createStopKeyword($info)
	{
		global $wpdb;
		return $wpdb->insert( $this->getTableName(), $info);
	}
	
	/**
     * get listing of stopwords
     * @global wpdb $wpdb
     * @param int $total
     * @param int $offset
     * @param string $sort
     * @return array
     */
	public function getListOfStopKeywords($total = 30, $offset = 0, $sort = 'id')
	{
		global $wpdb;
		return $wpdb->get_results("
			SELECT id,language, keyword, created
			FROM `".$this->getTableName()."` as t
                ORDER BY ".$sort."
                LIMIT ".$offset.", " . $total . " 
                ");
	}
    
    /**
     * get count of stopwords in DB
     * @global wpdb $wpdb
     * @return int
     */
	public function getTotalCount()
	{
		global $wpdb;
		return $wpdb->get_var("
			SELECT count(id)
			FROM `".$this->getTableName()."`");
	}
