    <h2 class="vidseo_heading">Method 1: Generate Transcript of a YouTube Video</h2>
    <ul>
        <li>Go to YouTube and open the video of your choice.</li>
        <li>Click on the More actions button (represented by 3 horizontal dots) located next to the Share button</li>
        <li>Now click on the Transcript option</li>
        <li>A transcript of the closed captions will automatically be generated</li>
        <li>Copy the content and paste it to Vidseo FREE editior or Vidseo PRO visual editor.</li>
    </ul>

